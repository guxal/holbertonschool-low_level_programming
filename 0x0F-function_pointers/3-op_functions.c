/**
 * op_add - sum
 * @a: integer 1
 * @b: integer 2
 * Return: integer
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - op sub
 * @a: integer 1
 * @b: integer 2
 * Return: integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - op mul
 * @a: integer 1
 * @b: integer 2
 * Return: integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - op div
 * @a: integer 1
 * @b: integer 2
 * Return: integer
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - op mod
 * @a: integer 1
 * @b: integer 2
 * Return: integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
